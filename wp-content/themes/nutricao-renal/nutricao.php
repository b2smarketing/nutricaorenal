				<p><b>Principais mudanças durante a progressão da doença</b><br>
				Existem grandes diferenças nas recomendações nutricionais durante a fase de pré-diálise e a fase de diálise da doença renal crônica.</p><br>

				<p><b>1.Prediálise</b></p>
				<p>Nos estádios de pré-diálise da doença renal crônica, é aconselhável <b>reduzir sua ingestão diária de proteínas substancialmente</b> em comparação com sua dieta familiar. Grandes quantidades de proteína prejudicariam as <b>nephrons</b> em seus rins. Toda proteína ingerida acima do requisito diário aumentará a aparência de <b>sintomas uraemicos</b> e aumentará a progressão da doença renal. Por estas razões, pelo menos, a proteína deve ser limitada à ingestão recomendada de 0,7-0,8 g / kg de peso corporal / dia ideal.</p><p>No entanto, ainda mais eficiente é uma ingestão marcada de proteína restrita (0,3 / 0,4-0,6 g / kg de peso corporal / dia) suplementada com <b>ácidos cetoicos essenciais / aminoácidos.</b> Isto é indicado com um declínio da <b>TFG</b> abaixo do valor de 60 a 50 ml / min. Ambos os tipos de restrição de proteína dietética garantem que você permanecerá em um bom estado nutricional. A este respeito, note que você precisa receber quantidades suficientes de energia, bem como quantidades adequadas de outros nutrientes essenciais (por exemplo, minerais e vitaminas).</p>

