				<p><b>2. Diálise</b></p>
				<p>Quando o tratamento de diálise se torna necessário, seu suporte alimentar deve mudar completamente. Devido ao aumento das perdas de proteínas através do procedimento de diálise e do excesso de catabolismo secundário à sessão de hemodiálise, <b>sua ingestão diária de proteína deve ser aumentada</b> para 1,2 g de proteína / kg de peso corporal / dia ou mais.</p>
				<p>Sua ingestão diária de energia deve permanecer constante em 30-35 kcal / kg de peso corporal / dia ou, de preferência, maior de acordo com sua atividade física ou se tiver menos de 60 anos de idade. Mais importante ainda se torna o monitoramento da ingestão de fluidos e de compostos específicos, como fosfato e potássio. Com relação ao fosfato, este é um tópico complicado: o aumento da ingestão de proteína na dieta sempre é paralelo com uma maior ingestão de fosfato! Por favor, peça o apoio de um nutricionista treinado para esclarecer essas dificuldades e para receber ajuda no planejamento de suas dietas.</p>

