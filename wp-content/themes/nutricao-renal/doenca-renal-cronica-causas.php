				<h2>Glomerulonefrite pode causar doença renal crônica</h2>
				<p>Glomerulonefrite é uma doença renal, em que os glomérulos (filtradores) ficam inflamados e cicatrizados, perdendo devagar a habilidade de remover os metabólitos e o excesso de água do corpo. Diversos tipos diferentes de doença renal estão agrupados juntos nessa categoria. Glomerulonefrite pode causar doença renal crônica</p>
				<p>A presença de sangue, proteínas ou ambos na urina é, frequentemente, o primeiro sinal de glomerulonefrite. Altas concentrações de proteínas podem destruir a função renal vagarosamente. O tratamento foca em diminuir o progresso da doença e prevenir complicações.</p>
				<p>Um tratamento apropriado vai ser designado pelo seu médico baseado na sua condição, sintomas e pode incluir controle da pressão arterial e modificação na dieta, assim como a o tratamento dialítico.</p>	
