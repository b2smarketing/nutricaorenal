				<h2>Diabetes Mellitus pode causar Doença Renal Crônica</h2>
				<p>Diabetes mellitus (Diabetes)  é uma doença, na qual os níveis de glicose no sangue estão acima do normal. Normalmente, quando você se alimenta o corpo quebra os carboidratos dos alimentos e o transforma em glicose, a qual é transportada pela corrente sanguínea ao longo do corpo. As células necessitam de glicose para produzir energia – esse é seu combustível. A entrada da glicose nas células é mediada pela insulina, um hormônio produzido no pâncreas. Devido a isso, a insulina ajuda a manter a concentração de glicose em níveis ideais. </p>
				<p>No Diabetes, a capacidade do corpo em produzir e/ou utilizar a insulina está comprometida. Como resultado, a concentração de glicose no sangue aumenta demasiadamente e as células não recebem glicose. Mesmo que as células estejam “famintas” pela glicose elas precisam trabalhar e isso, se não controlado pode levar a problemas sérios.</p>	
				<p>Seu médico deve ter lhe contado que existem dois tipos de diabetes, denominados de tipo 1 e tipo 2.
				O diabetes tipo 1, seu corpo não produz insulina ou a produção é insuficiente. Isso é denominado de deficiência insulínica.</p>
				<p>O diabetes do tipo 2, seu corpo produz insulina o suficiente. Porém, suas células estão incapazes de usar essa insulina. Nesse caso, as células só respondem à insulina quando ela está em alta concentração. Isso é denominado de resistência insulínica.</p>
