				<h3 class="subtopic">Como saber se você é portador de diabetes? Quais são os sintomas?</h3>
				<p>O diabetes do tipo 1 tem início súbito, logo na infância ou na adolescência. Pacientes portadores do diabetes do tipo 2, frequentemente, não sabem que são portadores de uma doença. A doença tem um início sútil  e, geralmente, é diagnosticada em um exame de sangue de rotina. </p>
				<p>Os possíveis sintomas do diabetes são:</p>
				<ul class="listoption">
					<li>Sede excessiva (Polidipsia);</li>
					<li>Aumento da frequência urinária (poliúria, nictúria); </li>
					<li>Fadiga;</li>
					<li>Câimbras na panturrilha à noite;</li>
					<li>Visão turva; </li>
					<li>Coceira, frequentemente, nos genitais e no ânus, infecções;</li>
					<li>Impotência;</li>
					<li>Interrupção do fluxo menstrual (amenorreia); </li>
					<li>Perda de peso (somente na diabetes tipo 1);</li> 
					<li>Fome excessiva, suor, dores de cabeça (começo da diabetes tipo 2);</li>
				</ul>
				<p>Nos casos extremos, os pacientes portadores de diabetes podem manifestar:</p>
				<ul class="listoption">
					<li>Desidratação, pele elástica e seca, língua seca e lábios rachados.</li>
					<li>Pulso rápido;</li>
					<li>Pressão baixa;</li>
					<li>Hálito cetônico;</li>
					<li>Apatia/coma; </li>
				</ul>
