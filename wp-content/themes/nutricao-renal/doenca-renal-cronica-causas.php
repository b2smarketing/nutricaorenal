				<h3 class="subtopic">Como que o diabetes afeta os rins?</h3>
				<p>Uma grande concentração de glicose no sangue de forma constante pode causar complicações em diversos órgãos como o coração, olhos, nervos e rins. Elevadas concentrações de glicose quando adentram nos rins são capazes de danificar os microcapilares. Com esse dano, os rins diminuem sua capacidade de excretar os metabólitos do sangue entrando em um quadro denominado de nefropatia diabética, a qual é considerada uma doença renal crônica. </p>
				<p>Se você é portador do diabetes deve controlar a concentração de glicose precisamente para diminuir a probabilidade de desenvolver a doença renal crônica, assim como outras consequências secundárias ao diabetes.</p>
