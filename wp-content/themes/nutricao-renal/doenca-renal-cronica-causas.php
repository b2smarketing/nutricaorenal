				<h3 class="subtopic">Quais testes podem ser feitos para diagnosticar o diabetes?</h3>
				<p>Um exame simples de sangue para checar a concentração de glicose é a maneira mais fácil de diagnosticar o diabetes. Concentrações normais de glicose estão em torno de 65-100mg/dl em jejum. Concentrações de glicose maiores que 126mg/dl depois de uma noite de jejum ou maior que 200mg/dl depois de duas horas de uma ingestão de 75g de um suplemento de glicose diagnosticam o diabetes.Sua urina também pode ser examinada. A urina não deve conter mais que 15mg de glicose por 100ml e não pode conter corpos cetônicos. </p>
