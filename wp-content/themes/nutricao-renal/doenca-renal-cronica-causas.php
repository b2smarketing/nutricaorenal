				<h2>Rim Policístico um exemplo de doença renal herdada e congênita</h2>
				<p>Algumas doenças renais são resultados de fatores hereditários. Rim policístico, por exemplo, é uma desordem genética em que diversos cistos crescem nos rins. Os cistos podem vagarosamente substituir o tecido funcional dos rins reduzindo a função renal podendo levar à falência renal.</p>
				<p>Alguns problemas renais podem aparecer antes mesmo do nascimento. Exemplos disso, incluem o rim policístico autossômico recessivo, uma forma mais rara de rim policístico, e outros problemas que interfiram na formação dos néfrons. Os sinais de doenças renais nas crianças variam. A criança pode crescer mais devagar, vomitar de forma frequente ou pode apresentar dores nas costas. Algumas doenças renais podem ser silenciosas por meses e até anos.</p>
				<p>Se a sua criança tem uma doença renal, seu pediatra deve detectar durante um check-up de rotina. Certifique-se que sua criança visite o pediatra regularmente.  Os primeiros sinais de doença renal podem ser hipertensão, menor número de células vermelhas no sangue (anemia) assim como, também, sangue e proteínas na urina.  Se o médico encontrar algum desses sintomas, mais exames se tornam necessários como de sangue, urina e exames de imagem. Em alguns casos, o médico pode realizar uma biopsia – removendo um pequeno pedaço do rim para examiná-lo no microscópio.</p>
				<p>Algumas doenças renais hereditárias podem não ser detectadas até a idade adulta. A forma mais comum de rim policístico era denominada de rim policístico adulto, pelo fato dos sintomas de pressão alta e falência renal, geralmente, não ocorrem até os pacientes completarem seus vinte, trinta anos. Mas, com os avanços dos diagnósticos por imagem, os médicos são aptos a detectar cistos nas crianças e em adolescentes antes mesmo do início dos sintomas.</p>	
